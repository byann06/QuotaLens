export const translate = (language, key) => {
  const safeLanguage = normalizeLanguage(language);

  return translations[safeLanguage]?.[key] ?? translations.en[key] ?? key;
};

export const formatTranslation = (language, key, values = {}) =>
  translate(language, key).replace(/\{(\w+)\}/g, (match, token) =>
    Object.prototype.hasOwnProperty.call(values, token) ? String(values[token]) : match,
  );
